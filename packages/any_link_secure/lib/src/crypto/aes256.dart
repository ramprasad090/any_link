import 'dart:typed_data';

/// Pure-Dart AES-256 in CTR mode — no third-party dependencies.
///
/// AES-256-CTR is semantically secure and stream-cipher-like (encrypt == decrypt).
/// Used by [PayloadEncryptor] for payload confidentiality.
///
/// Key: 32 bytes. Nonce/IV: 16 bytes (used as initial counter block).
class Aes256Ctr {
  // AES S-box.
  static const List<int> _sbox = [
