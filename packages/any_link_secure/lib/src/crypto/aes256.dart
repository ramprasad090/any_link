  // Rcon for key expansion.
  static const List<int> _rcon = [
    0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1b,0x36,
    0x6c,0xd8,0xab,0x4d,0x9a,0x2f,0x5e,0xbc,0x63,0xc6,
    0x97,0x35,0x6a,0xd4,0xb3,0x7d,0xfa,0xef,0xc5,0x91,
  ];

  static int _mul(int a, int b) {
    int p = 0;
    for (var i = 0; i < 8; i++) {
      if ((b & 1) != 0) p ^= a;
      final hiBit = (a & 0x80) != 0;
      a = (a << 1) & 0xff;
      if (hiBit) a ^= 0x1b;
      b >>= 1;
    }
    return p;
  }

  /// Expand a 32-byte AES-256 key into 240 bytes of round keys.
  static Uint8List expandKey(List<int> key) {
    assert(key.length == 32, 'AES-256 requires a 32-byte key');
    final w = Uint8List(240);
    w.setRange(0, 32, key);
    for (var i = 8; i < 60; i++) {
      int t0 = w[(i - 1) * 4];
      int t1 = w[(i - 1) * 4 + 1];
      int t2 = w[(i - 1) * 4 + 2];
      int t3 = w[(i - 1) * 4 + 3];
      if (i % 8 == 0) {
        // RotWord + SubWord + Rcon.
        final tmp = t0;
        t0 = _sbox[t1] ^ _rcon[i ~/ 8 - 1];
        t1 = _sbox[t2];
        t2 = _sbox[t3];
        t3 = _sbox[tmp];
      } else if (i % 8 == 4) {
        t0 = _sbox[t0]; t1 = _sbox[t1]; t2 = _sbox[t2]; t3 = _sbox[t3];
      }
      w[i * 4]     = w[(i - 8) * 4]     ^ t0;
      w[i * 4 + 1] = w[(i - 8) * 4 + 1] ^ t1;
      w[i * 4 + 2] = w[(i - 8) * 4 + 2] ^ t2;
      w[i * 4 + 3] = w[(i - 8) * 4 + 3] ^ t3;
    }
    return w;
  }

  /// Encrypt a single 16-byte block with the given expanded key schedule.
  static Uint8List encryptBlock(List<int> block, Uint8List roundKeys) {
    assert(block.length == 16);
    final s = List<int>.from(block);

    // Initial AddRoundKey.
    for (var i = 0; i < 16; i++) s[i] ^= roundKeys[i];

    for (var round = 1; round <= 14; round++) {
      // SubBytes.
      for (var i = 0; i < 16; i++) s[i] = _sbox[s[i]];

      // ShiftRows.
      final tmp = List<int>.from(s);
      s[1]  = tmp[5];  s[5]  = tmp[9];  s[9]  = tmp[13]; s[13] = tmp[1];
      s[2]  = tmp[10]; s[6]  = tmp[14]; s[10] = tmp[2];  s[14] = tmp[6];
      s[3]  = tmp[15]; s[7]  = tmp[3];  s[11] = tmp[7];  s[15] = tmp[11];

      // MixColumns (skip on final round).
      if (round < 14) {
        for (var col = 0; col < 4; col++) {
          final i = col * 4;
          final a = s[i], b = s[i+1], c = s[i+2], d = s[i+3];
          s[i]   = _mul(a, 2) ^ _mul(b, 3) ^ c ^ d;
          s[i+1] = a ^ _mul(b, 2) ^ _mul(c, 3) ^ d;
          s[i+2] = a ^ b ^ _mul(c, 2) ^ _mul(d, 3);
          s[i+3] = _mul(a, 3) ^ b ^ c ^ _mul(d, 2);
        }
      }

      // AddRoundKey.
      for (var i = 0; i < 16; i++) s[i] ^= roundKeys[round * 16 + i];
    }

    return Uint8List.fromList(s);
  }

  /// AES-256-CTR encrypt/decrypt (symmetric). Returns ciphertext/plaintext.
  ///
  /// [key] must be 32 bytes. [nonce] must be 16 bytes (used as initial counter).
  static Uint8List ctr(List<int> data, List<int> key, List<int> nonce) {
    assert(key.length == 32, 'Key must be 32 bytes');
    assert(nonce.length == 16, 'Nonce must be 16 bytes');

    final roundKeys = expandKey(key);
    final result = Uint8List(data.length);
    final counter = List<int>.from(nonce);

    for (var offset = 0; offset < data.length; offset += 16) {
      final keystream = encryptBlock(counter, roundKeys);
      final blockLen = (data.length - offset).clamp(0, 16);
      for (var i = 0; i < blockLen; i++) {
        result[offset + i] = data[offset + i] ^ keystream[i];
      }
      // Increment counter (big-endian).
      for (var i = 15; i >= 0; i--) {
        counter[i] = (counter[i] + 1) & 0xff;
        if (counter[i] != 0) break;
      }
    }
    return result;
  }
}
